package basicsofjava;
// Write a program to print even & odd numbers from 0 to 100
public class Assign25_Shiva_39B {

	public static void main(String[] args) {

		for (int i = 0; i <= 100; i++)

		{
			if (i % 2 == 0) {

				System.out.println("even no" + i);
			} else {

				System.out.println("odd no" + i);
			}

		}

	}

}
