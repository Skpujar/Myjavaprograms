package basicsofjava;
// Swap the 2 numbers with extra variable 
public class Assgn53_Shiva_39B_Swap_Num_with3rdvariable {

	public static void main(String[] args) {
		int a=500;
		int b=200;
		int c=0;
		
		c=a;  // c=500
		a=b;  // a=200
		b=c;  // b=500  
		
		System.out.println(a+ "a---> value ");
		System.out.println(b+ "b---->value ");

	}

}
